"""attention_block_2d — pass_5

A *real model* this time, not just a synthetic matrix reader.

Two hand-set pieces, both on CUDA, no learning anywhere:

  1. PRODUCER — `Block2DAttention`: a single-head 2D attention block that is a
     small delta from `experiments/base_model.py`. base_model's `Attention`
     computes content scores `q·kᵀ` then softmax. Here the content term is
     switched off (q,k ≡ 0) and the spatial structure lives entirely in a
     **hand-set additive bias** — the Swin-style *relative-position-bias table*
     for local/dilated windows, a global-token bias column/row for `global`,
     and a causal mask for `causal_2d`. `softmax(bias + tiny noise)` yields the
     row-stochastic attention matrix. The MLP is dropped (dead weight for this
     task — see README). The bias table IS the mechanism.

  2. READER — `classify`: a hand-built geometric interpretability method that
     recovers `(family, params)` from an attention matrix ALONE. It never
     consults the producer's bias nor the task's private generators. It reads
     key−query displacement offsets (→ window size & dilation), a full
     row+column with otherwise-sparse rows (→ global token), or a lower-
     triangular mask (→ 2D causal).

The READER is what the task scores (canonical seed-0 batch of 16, generated by
the task's own generator). On top of that we run two experiments the previous
pass only *described*:

  • FAITHFULNESS — a genuine causal ablation: zero the producer's bias table and
    re-read. The pattern collapses to uniform attention and the reader's verdict
    breaks (4/4 → 0/4 correct). The behaviour causally depends on the bias.

  • OPERATING RANGE — drive the producer across grids from 4×4 (N=16) to 32×32
    (N=1024) — a 64× span — and every in-range param combo, then read each back.
    Reports reader accuracy vs the majority baseline as a function of N.

Everything below runs in torch on CUDA.
"""

from __future__ import annotations

import json

import numpy as np
import torch

from agentic.experiments import load_task, record_benchmark, results_dir

DEVICE = "cuda"

task = load_task(__file__)

FAMILIES = ["local", "dilated", "global", "causal_2d"]


# =============================================================================
# Grid geometry helpers (all on GPU)
# =============================================================================
def grid_disp(H: int, W: int):
    """Return key−query row/col displacement maps DR, DC of shape (N, N)."""
    N = H * W
    idx = torch.arange(N, device=DEVICE)
    rows = (idx // W).to(torch.long)
    cols = (idx % W).to(torch.long)
    DR = (rows[None, :] - rows[:, None]).to(torch.float32)
    DC = (cols[None, :] - cols[:, None]).to(torch.float32)
    return rows, cols, DR, DC


def allowed_mask(family: str, H: int, W: int, params: dict) -> torch.Tensor:
    """Boolean (N, N) mask of allowed (query, key) pairs for a pattern family.

    This is the *ideal* footprint. The producer turns it into an additive bias;
    the reader rebuilds it from its inferred params to score confidence. Same
    geometry used by both ends, kept honest by being parameterised only.
    """
    N = H * W
    _, _, DR, DC = grid_disp(H, W)
    if family == "local":
        ws = int(params["window_size"])
        m = (DR.abs() <= ws + 0.5) & (DC.abs() <= ws + 0.5)
    elif family == "dilated":
        ws = int(params["window_size"])
        dil = int(params["dilation"])
        DRl = DR.round().to(torch.long)
        DCl = DC.round().to(torch.long)
        m = (
            (DRl % dil == 0)
            & (DCl % dil == 0)
            & (DR.abs() <= ws * dil + 0.5)
            & (DC.abs() <= ws * dil + 0.5)
        )
    elif family == "global":
        p = int(params["global_pos"])
        m = torch.zeros(N, N, dtype=torch.bool, device=DEVICE)
        m[p, :] = True
        m[:, p] = True
    elif family == "causal_2d":
        ar = torch.arange(N, device=DEVICE)
        m = ar[None, :] <= ar[:, None]
    else:
        raise ValueError(family)
    return m


# =============================================================================
# PRODUCER — hand-set 2D attention block (delta from base_model.Attention)
# =============================================================================
def produce(family: str, H: int, W: int, params: dict, seed: int,
            ablate: bool = False, noise_std: float = 0.02) -> torch.Tensor:
    """Run the hand-set attention block forward → row-stochastic (N, N) matrix.

    scores = additive_bias + tiny_noise ;  A = softmax(scores, dim=1)

    `additive_bias` is +0 on allowed keys, −1e9 on disallowed (a hard Swin-style
    relative-position-bias table / global bias / causal mask). `ablate=True`
    deletes the bias (all-zero), the causal-knockout for the faithfulness test.
    """
    N = H * W
    g = torch.Generator(device=DEVICE).manual_seed(seed)
    noise = noise_std * torch.randn(N, N, generator=g, device=DEVICE)
    if ablate:
        scores = noise                                   # bias removed
    else:
        m = allowed_mask(family, H, W, params)
        bias = torch.zeros(N, N, device=DEVICE)
        bias[~m] = -1.0e9
        scores = bias + noise
    A = torch.softmax(scores, dim=1)
    return A


# =============================================================================
# READER — geometric interpretability method (matrix-only)
# =============================================================================
def classify(A: torch.Tensor) -> dict:
    """Recover (pattern_id, params, confidence) from an attention matrix alone."""
    N = A.shape[0]
    side = int(round(N ** 0.5))
    H = W = side
    _, _, DR, DC = grid_disp(H, W)

    total = A.sum()
    rowmax = A.max(dim=1, keepdim=True).values
    M = (A > 0.5 * rowmax).to(torch.float32)             # attended-key mask
    rowcount = M.sum(dim=1)
    colcount = M.sum(dim=0)
    full_row = rowcount >= (N - 0.5)
    full_col = colcount >= (N - 0.5)

    def conf_for(fam, params):
        mask = allowed_mask(fam, H, W, params).to(torch.float32)
        return float(((A * mask).sum() / total).clamp(0, 1).item())

    # ---- GLOBAL: index p has a full row AND full column, and almost every
    #      other row is single-key (the hallmark of a global token) -----------
    sparse = int((rowcount <= 1.5).sum().item())
    cand = (full_row & full_col).nonzero(as_tuple=False).flatten()
    if cand.numel() > 0 and sparse >= N - 1.5:
        p = int(cand[0].item())
        return {"pattern_id": "global", "params": {"global_pos": p},
                "confidence": conf_for("global", {"global_pos": p})}

    # ---- CAUSAL_2D: binary mask equals the lower-triangular raster mask ------
    ar = torch.arange(N, device=DEVICE)
    tril = (ar[None, :] <= ar[:, None]).to(torch.float32)
    if float((M == tril).to(torch.float32).mean().item()) > 0.97:
        return {"pattern_id": "causal_2d", "params": {},
                "confidence": conf_for("causal_2d", {})}

    # ---- LOCAL / DILATED: read positive displacement offsets ----------------
    mb = M.bool()
    offs = torch.cat([DR[mb].abs(), DC[mb].abs()])
    offs = offs[offs > 0.5]
    if offs.numel() == 0:
        ws, dil = 1, 1
    else:
        pos = torch.unique(offs)                         # sorted ascending
        dil = int(round(float(pos[0].item())))
        ws = int(pos.numel())

    if dil > 1:
        params = {"window_size": ws, "dilation": dil}
        return {"pattern_id": "dilated", "params": params,
                "confidence": conf_for("dilated", params)}
    params = {"window_size": ws}
    return {"pattern_id": "local", "params": params,
            "confidence": conf_for("local", params)}


def model_fn(attn: np.ndarray) -> dict:
    """Task entry point: NumPy matrix in → prediction dict out (reader on GPU)."""
    A = torch.as_tensor(attn, dtype=torch.float32, device=DEVICE)
    return classify(A)


# =============================================================================
# Experiment glue
# =============================================================================
def _params_match(pred: dict, gt_fam: str, gt_params: dict) -> bool:
    return all(pred.get(k) == v for k, v in gt_params.items())


def run_faithfulness() -> dict:
    """Causal ablation of the producer's bias table, on the 8×8 canonical params."""
    H = W = 8
    canon = {
        "local": {"window_size": 1},
        "dilated": {"window_size": 1, "dilation": 2},
        "global": {"global_pos": 0},
        "causal_2d": {},
    }
    rows = []
    ex_intact, ex_ablated = [], []
    for fam in FAMILIES:
        params = canon[fam]
        A = produce(fam, H, W, params, seed=100)
        A_ab = produce(fam, H, W, params, seed=100, ablate=True)
        p_in = classify(A)
        p_ab = classify(A_ab)
        ok_in = (p_in["pattern_id"] == fam) and _params_match(p_in["params"], fam, params)
        ok_ab = (p_ab["pattern_id"] == fam) and _params_match(p_ab["params"], fam, params)
        rows.append({
            "family": fam, "params": params,
            "intact_pred": p_in["pattern_id"], "intact_params": p_in["params"],
            "intact_correct": bool(ok_in), "intact_conf": p_in["confidence"],
            "ablated_pred": p_ab["pattern_id"], "ablated_params": p_ab["params"],
            "ablated_correct": bool(ok_ab), "ablated_conf": p_ab["confidence"],
        })
        ex_intact.append(A.detach().cpu().numpy())
        ex_ablated.append(A_ab.detach().cpu().numpy())
    intact_acc = sum(r["intact_correct"] for r in rows) / len(rows)
    ablated_acc = sum(r["ablated_correct"] for r in rows) / len(rows)
    examples = {
        "families": FAMILIES,
        "intact": np.stack(ex_intact),
        "ablated": np.stack(ex_ablated),
    }
    return {"rows": rows, "intact_acc": intact_acc, "ablated_acc": ablated_acc}, examples


def op_params(side: int):
    """In-range param combos for a square grid (windows kept strictly smaller
    than the grid so 'attend-everyone' saturation can't masquerade as global)."""
    out = []
    for ws in (1, 2, 3):
        if 2 * ws + 1 < side:
            out.append(("local", {"window_size": ws}))
    for ws in (1, 2):
        dil = 2
        if 2 * ws * dil + 1 < side:
            out.append(("dilated", {"window_size": ws, "dilation": dil}))
    out.append(("global", {"global_pos": 0}))
    out.append(("global", {"global_pos": side * side - 1}))
    out.append(("causal_2d", {}))
    return out


def run_operating_range() -> dict:
    """Drive the producer across N=16 … 1024 and read every example back."""
    sides = [4, 6, 8, 12, 16, 24, 32]
    records = []
    for side in sides:
        H = W = side
        for fam, params in op_params(side):
            for r in range(3):                           # 3 noise realisations
                A = produce(fam, H, W, params, seed=1000 * side + 7 * r)
                pred = classify(A)
                ok = (pred["pattern_id"] == fam) and _params_match(pred["params"], fam, params)
                records.append({
                    "side": side, "N": side * side, "family": fam,
                    "params": params, "pred": pred["pattern_id"],
                    "pred_params": pred["params"], "correct": bool(ok),
                    "confidence": pred["confidence"],
                })
    by_grid = []
    for side in sides:
        sub = [r for r in records if r["side"] == side]
        if not sub:
            continue
        acc = sum(r["correct"] for r in sub) / len(sub)
        # majority baseline within this grid = fraction that are 'local'
        base = sum(1 for r in sub if r["family"] == "local") / len(sub)
        by_grid.append({"side": side, "N": side * side, "acc": acc,
                        "n": len(sub), "baseline_acc": base})
    overall = sum(r["correct"] for r in records) / len(records)
    base_overall = sum(1 for r in records if r["family"] == "local") / len(records)
    return {"records": records, "by_grid": by_grid,
            "overall_acc": overall, "baseline_acc": base_overall}


def main() -> None:
    run_dir = results_dir(__file__)
    print(f"[pass_5] run dir: {run_dir}")

    # --- headline: reader on the task's canonical seed-0 batch ---------------
    payload = task.evaluate(model_fn)
    n_correct = sum(1 for r in payload["sweep"] if r["correct"])
    print(f"[pass_5] canonical reader accuracy: {n_correct}/{len(payload['sweep'])}")
    record_benchmark(__file__, run_dir, payload)

    # --- faithfulness: causal ablation of the producer's bias table ----------
    faith, examples = run_faithfulness()
    print(f"[pass_5] faithfulness  intact={faith['intact_acc']:.2f}  "
          f"ablated={faith['ablated_acc']:.2f}")
    (run_dir / "faithfulness.json").write_text(json.dumps(faith, indent=2))
    np.savez(run_dir / "faith_examples.npz",
             families=np.array(examples["families"]),
             intact=examples["intact"], ablated=examples["ablated"])

    # --- operating range: N=16 … 1024 ---------------------------------------
    op = run_operating_range()
    print(f"[pass_5] operating range  overall={op['overall_acc']:.2f}  "
          f"baseline={op['baseline_acc']:.2f}  (N=16..1024)")
    (run_dir / "operating_range.json").write_text(json.dumps(op, indent=2))

    print("[pass_5] done.")


if __name__ == "__main__":
    main()
